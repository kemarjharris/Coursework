int H_cost_nokitty(int x, int y, int cat_loc[10][2], int cheese_loc[10][2], int mouse_loc[1][2], int cats, int cheeses, double gr[graph_size][4])
{
	// this follows the same idea as before, but now, we're avoiding casts as much as possible.
    // the idea is to move away from the closest cat.
    // take the maximum distance from the cat, and subtract the current manhattan distance from the mouse.
    // the manhattan distance will be more than the euclidean distance, so avoiding cats takes priorty over getting the cheese.

    int mouse_x = mouse_loc[0][0];
    int mouse_y = mouse_loc[0][1];

    int min_cat = closest(mouse_x, mouse_y, cat_loc, cats);

    int cat_x = cat_loc[min_cat][0];
    int cat_y = cat_loc[min_cat][1];
    // inverted so it returns the maximal distance
    int distance[graph_size];
    int next_index = TO_INDEX(x, y);
    set_distances(gr, distance, next_index);

    int cat_index= TO_INDEX(cat_x, cat_y);
    int d = - (distance[cat_index] + 1);

    return d;
}

// finds distance of closest object using manhattan distance
int closest(int x, int y, int thing_loc[10][2], int things) {
    int min_dist = INT_MAX;

    int min_thing = -1;

    int thing_x;
    int thing_y;
    // pick the distance of the cat thats closest to the mouse
    for (int i = 0; i < things; i++) {
        thing_x = thing_loc[i][0];
        thing_y = thing_loc[i][1];

        int d = abs(thing_x - x) + abs(thing_y - y);
        if (d < min_dist) {
            min_thing = i;
        }
    }
    return min_thing;
}

int cat_position(int index, int cat_loc[10][2], int cats) {
    int x = X_VALUE(index);
    int y = Y_VALUE(index);

    for (int i = 0; i < cats; i++) {
        if (cat_loc[i][0] == x && cat_loc[i][1] == y) {
            return 1;
        }
    }
    return 0;
}


int euclidian_distance(int x1, int y1, int x2, int y2) {
    double x = x2 - x1;
    double y = y2 - y1;
    double d = sqrt( pow(x, 2) + pow(y, 2));
    d = floor(d);
    int ret = d;
    return ret;
}

// a very similar copy of BFS that finds and sets the distance for all values in the graph using the distance array.
void set_distances(double graph[graph_size][4], int distance[graph_size], int index) {
    
    distance[index] = 0;
    int priority_queue[graph_size][2];
    init_queue(priority_queue);

    int seen[graph_size];
    init_seen(seen);

    update_queue(priority_queue, index, 0);

    while (!queue_is_empty(priority_queue)) {
        // get next node u from pq
        int u = get_next(priority_queue);
        // mark u as seen
        add_to_set(seen, u);

        for (int i = 0; i < 4; i ++) {
            if (graph[u][i]) {
                int v = connected_index(u, i);
                if (!set_contains(seen, v)) {
                    // add the value to queue
                    update_queue(priority_queue, v, 0);
                    // mark seen
                    add_to_set(seen, v);
                    // set distance
                    distance[v] = distance[u] + 1;
                }
            }
        }
    }
}

void update_visited(int index_visited, int visit_order[size_X][size_Y], int num_visited) {
    int x = X_VALUE(index_visited);
    int y = Y_VALUE(index_visited);

    visit_order[x][y] = num_visited;
}

void init_seen(int seen[graph_size]) {
    for (int i = 0; i < graph_size; i++) 
    {
        seen[i] = -1;
    }
}

int connected_index(int index, int direction) {
    int x = X_VALUE(index);
    int y = Y_VALUE(index);
    if (direction == 0) {
        y --;
    } else if (direction == 1) {
        x ++;
    } else if (direction == 2) {
        y ++;
    } else if (direction == 3) {
        x --;
    }

    if (x < 0 || y < 0 || x >= size_X || y >= size_Y) {
        printf("incorrect value of (%d, %d) at index %d in direction %d\n", x, y, index, direction);
        exit(1);
    }

    return TO_INDEX(x, y);
}

void add_to_set(int s[set_size], int value) {
    for (int i = 0; i < set_size; i ++) {
        if (s[i] == value) {
            return;
        } else if (s[i] == -1) {
            s[i] = value;
            return;
        }
    }
}

int set_contains(int s[set_size], int value) {
    for (int i = 0; i < set_size; i ++) {
        if (s[i] == value) {
            return 1;
        }
    }
    return 0;
}

int get_next(int q[][2]){
	int minimum_priority = INT_MAX;
	int index = -1;
	for (int i = 0; i < queue_size; i++) {
		// spot is not empty and smallest priority
		if (q[i][0] > -1 && q[i][1] < minimum_priority) {
			minimum_priority = q[i][1];
			index = i;
		}
	}
	if (index < 0) {
		return -1;
	}
	int ret_index = q[index][0];
	q[index][0] = -2; // spot taken, but not valid, necesarry for normal queue behaviour
	q[index][1] = INT_MAX;
	return ret_index;;
}

void update_queue(int q[][2], int value, int priority) {
	int free_spot = -1;
	for (int i = 0; i < queue_size; i++) {
		if (q[i][0] == value) {
			if (q[i][1] > priority) {
				q[i][1] = priority;
			}
			return;
		} else if (free_spot == -1 && q[i][0] == -1) {
			free_spot = i;
		}
	}
	// not in queue so update
	q[free_spot][0] = value;
	q[free_spot][1] = priority;
}

void init_queue(int q[][2]) {
    for (int i = 0; i < queue_size; i ++) 
    {
        q[i][0] = -1;
        q[i][1] = INT_MAX;
    }
}

int queue_is_empty(int q[][2]) {
    for (int i = 0; i < queue_size; i ++)
    {
        if (q[i][0] > -1) {
            return 0;
        }
    }
    return 1;
}
