void BFS(double graph[graph_size][4], int start, int goal, int path[graph_size][2], int visit_order[size_X][size_Y], int cat_loc[10][2], int cats) {
    // bfs code
    // implementation of BFS

    int seen[graph_size];
    int pred[graph_size];
    int priority_queue[graph_size][2];
    int num_visited = 1;
    

    init_seen(seen);
    init_seen(pred);
    init_queue(priority_queue);

	// add start node to queueint priority_queue[graph_size][2];
    update_queue(priority_queue, start, 0);
	// found marker
    int found = 0;
	// while the queue is not empty and not found
    while (!queue_is_empty(priority_queue) && !found)
    {

        
        // get next node u from pq
        int u = get_next(priority_queue);
        // mark u as seen
        add_to_set(seen, u);

        update_visited(u, visit_order, num_visited);
        num_visited ++;

        if (u == goal) {
            found = 1;
        } else { // search
            // for all nodes v in u's adjacency list
            for (int i = 0; i < 4; i ++) {
                
                if (graph[u][i]) {
                    // re add v to queue, queue does not enable duplicates
                    int v = connected_index(u, i);
                    if (!set_contains(seen, v) && !cat_position(v, cat_loc, cats)) {
                        // set to discovered
                        add_to_set(seen, v);
                        // update the queu, bfs ignores priorty
                        update_queue(priority_queue, v, 0);
                        pred[v] = u;
                        
                    }
                }
            }   
        }
    }

    if (found) {
        int i = 0;
        int reverse_path[graph_size];
        int index = goal;
        while (index != start)
        {
            reverse_path[i] = index;
            index = pred[index];
            i ++;
        }
        reverse_path[i] = start;
        
        int j = 0;
        for (; i >= 0; i--) {
            int pos = reverse_path[i];
            path[j][0] = X_VALUE(pos);
            path[j][1] = Y_VALUE(pos);
            j ++;
        }
    }
}

